public class Voiture {
	int code;
	String pilote;
	double vitesse;
	double reservoir;
	double consoKm;
	double fiabilite;
	
	public Voiture(int code, String pilote, double vitesse, double reservoir, double consoKm, double fiabilite) {
		this.code = code;
		this.pilote = pilote;
		this.vitesse = vitesse;
		this.reservoir = reservoir;
		this.consoKm = consoKm;
		this.fiabilite = fiabilite;
	}
}